// Operators

/**
 1. Arithmetic Operators
 * Arithmetic operators are used to perform mathematical operations on operands.
 * The supported arithmetic operators are:
 * - Addition (+)
 * - Subtraction (-)
 * - Multiplication (*)
 * - Division (/)
 * - Modulus (%)
 * - Increment (++)
 * - Decrement (--) 
 */

/**
 2. Comparison Operators
 * Comparison operators are used to compare two values and return a boolean result.
 * The supported comparison operators are:
 * - Equal to (==)
 * - Not equal to (!=)
 * - Strict equal to (===) - check datatype also
 * - Strict not equal to (!==)
 * - Greater than (>)
 * - Greater than or equal to (>=)
 * - Less than (<)
 * - Less than or equal to (<=)
 */

/**
 // 3. Logical Operators
 * Logical operators are used to combine or invert boolean expressions and returns true or false .
 * The supported logical operators are:
 * - Logical AND (&&) - const isValidNumber = x > 8 && 8 > y
 * - Logical OR (||) - const isValidNumber = x > 8 || 8 > y
 * - Logical NOT (!) - const isValid = true, console.log(!isValid) - false
 */

/**
 // 4. String Operators
 * - (+) - concatenation
 */

/**
// 5. Ternary Operator
 - ( ?  :) - if else
 */
