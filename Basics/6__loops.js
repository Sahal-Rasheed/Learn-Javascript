// Loops

// 1. for Loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// 2. while Loop
let i = 10;
while (i < 15) {
    console.log(i);
    i++;
}

// 3. for of Loop - Loop over an Array - like (for x in list) in python
let fruits = ['apple', 'banana', 'orange'];
for (let fruit of fruits) {
    console.log(fruit);
}



